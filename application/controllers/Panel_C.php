<?php
defined('BASEPATH') OR exit('No direct script access allowed');

class Galeria_C extends CI_Controller {

	public function index(){
		$this->load->view('header/header_galeria');
		$this->load->view('galeria_V');
	}

	public function vistaAmpliada($Data){
		$Imagen['imagen'] = $Data; 

		$this->load->view('header/header_SoloEstilos');
		$this->load->view('vistaDetalle_V', $Imagen);
	}
}
